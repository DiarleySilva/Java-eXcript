        int inteiro = 1; // Variável para numeros inteiros
        boolean bool = true; // Variável para armazenar verdadeiro ou falso
        
        System.out.println(fracionais);
        System.out.println(texto);
        System.out.println(inteiro);
        System.out.println(bool);
